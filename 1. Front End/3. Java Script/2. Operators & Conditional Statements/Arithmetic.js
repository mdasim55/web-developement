let a=5;
let b=2;
console.log("a=",a," & b=",b);          // a=5 *b=2
console.log("a+b=",a+b);                // a+b=7
console.log("a-b=",a-b);                // a-b=3
console.log("a*b=",a*b);                // a*b=10
console.log("a/b=",a/b);                // a/b=2.5
console.log("a%b=",a%b);                // a%b=1
console.log("a**b=",a**b);              // a**b=25

console.log("a++=",a++);                // a++=5
console.log("a++=",a);                  // a++=5
console.log("b--=",b--);                // a--=2
console.log("b--=",b);                  // a--=1
a=5;
b=2;
console.log("++a=",++a);                // ++a=5
console.log("a=",a);                    // a=5
console.log("--b=",--b);                // --a=1
console.log("b=",b);                    // a=1